SELECT 
	Н_ЛЮДИ.ФАМИЛИЯ,
	Н_СЕССИЯ.ИД
FROM 
	Н_ЛЮДИ
RIGHT JOIN
	Н_СЕССИЯ
ON
	Н_ЛЮДИ.ИД = Н_СЕССИЯ.ЧЛВК_ИД
WHERE 
	Н_ЛЮДИ.ИМЯ = 'Николай'
AND 
	Н_СЕССИЯ.ЧЛВК_ИД = 100622
AND 
	Н_СЕССИЯ.ЧЛВК_ИД = 105948;


-- 2
SELECT
	Н_ЛЮДИ.ОТЧЕСТВО,
	Н_ОБУЧЕНИЯ.НЗК,
	Н_УЧЕНИКИ.ИД
FROM
	Н_ЛЮДИ
INNER JOIN
	Н_ОБУЧЕНИЯ
ON	
	Н_ЛЮДИ.ИД = Н_ОБУЧЕНИЯ.ЧЛВК_ИД
INNER JOIN
	Н_УЧЕНИКИ
ON
	Н_ОБУЧЕНИЯ.ЧЛВК_ИД = Н_УЧЕНИКИ.ЧЛВК_ИД
WHERE
	Н_ЛЮДИ.ОТЧЕСТВО = 'Георгиевич'
AND 
	Н_ОБУЧЕНИЯ.ЧЛВК_ИД < 112514;